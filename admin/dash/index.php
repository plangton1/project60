              <p>เนื่องจาก วว. เป็นหน่วยงานด้านวิทยาศาสตร์และเทคโนโลยี ที่มีศักยภาพทั้งทางด้านเครื่องมือและบุคลากรที่สามารถให้บริการกับภาคอุตสาหกรรมในด้านการวิเคราะห์ ทดสอบให้สินค้าเป็นไปตาม มอก. ก่อนที่จะออกจําหน่ายในท้องตลาด ดังนั้นเมื่อสมอ. มีการกําหนด มอก. ขึ้นมาใหม่หรือมีการแก้ไข จึงต้องการหน่วยงานที่สามารถตรวจสอบผลิตภัณฑ์ให้เป็นไปตามมาตรฐานที่กําหนดไว้ เพื่อให้รองรับภาคอุตสาหกรรมได้
            อย่างทั่วถึง ดังนั้น วว. รวมถึงหน่วยงานอื่นๆ ที่มีความพร้อมในการตรวจสอบผลิตภัณฑ์ จึงมีการยื่นขอรับการแต่งตั้งเป็นผู้ตรวจสอบผลิตภัณฑ์อุตสาหกรรม หรือได้รับเชิญโดยจดหมายจากทางสมอ. หากทางสมอ. มีการพิจารณาว่ามีความพร้อมในการให้บริการ</p>
