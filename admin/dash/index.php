              <p>สํานักงานมาตรฐานผลิตภัณฑ์อุตสาหกรรม (สมอ.) เป็นหน่วยงานหลักในการดําเนินงาน มีภารกิจหลักในการคุ้มครองอุตสาหกรรมภายในประเทศ
            และการคุ้มครองผู้บริโภคให้ได้รับผลิตภัณฑ์ที่มีคุณภาพ โดยการ<br>กําหนดมาตรฐานผลิตภัณฑ์อุตสาหกรรมในประเทศ และการควบคุมผลิตภัณฑ์
            อุตสาหกรรมทั้งที่ผลิตในประเทศและที่นําเข้าจากต่างประเทศให้มีคุณภาพตามมาตรฐานที่กําหนด การดําเนินงานของ สมอ.ในปัจจุบัน ได้ขยาย
            ขอบข่ายการดําเนินงานออกไปกว้างขวางยิ่งขึน โดยครอบคลุมภารกิจการกำหนดมาตรฐานอื่นที่ไม่จํากัดเฉพาะผลิตภัณฑ์อุตสาหกรรมด้วย เช่น
            ระบบการบริหารจัดการ บริการ บุคลากร หน่วยงาน การพัฒนาผู้ประกอบการ SMEs และวิสาหกิจชุมชน การเสริมสร้างขีดความสามารถในการ
            แข่งขันของผู้ประกอบการไทยในเวทีการค้าระหว่างประเทศ และการรับรองความสามารถของหน่วยงานตรวจสอบและรับรองตามมาตรฐานสากล</p>
