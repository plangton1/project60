              <h4  class="font-mirt"><a href="">มอก. คืออะไร</a></h4>
              <p>มอก.ย่อมาจาก “มาตรฐานผลิตภัณฑ์อุตสาหกรรม” (Thai Industrial Standard) หมายถึง ข้อกําหนดทางวิชาการที่สํานักงานมาตรฐานผลิตภัณฑ์
            อุตสาหกรรม (สมอ.) ได้กําหนดขึ้นเพื่อเป็นแนวทางแก่ผู้ผลิตในการผลิตสินค้าให้มีคุณภาพในระดับที่เหมาะสมกับการใช้งานมากที่สุด โดยจัดทําออกมาเป็นเอกสารและจัดพิมพ์เป็นเล่ม ภายใน มอก. แต่ละเล่มประกอบด้วยเนื้อหาที่เกี่ยวข้องกับการผลิตผลิตภัณฑ์นั้นๆ เช่น เกณฑ์ทาง
            เทคนิค คุณสมบัติที่สําคัญ ประสิทธิภาพของการนําไปใช้งาน คุณภาพของวัตถุที่นํามาผลิต และวิธีการทดสอบเป็นต้น สินค้าที่กําหนดเป็น
            มาตรฐานปัจจุบัน ครอบคลุมสินค้าที่ใช้อยู่ในชีวิตประจําวันหลาย ๆ ประเภท ได้แก่ ประเภทอาหาร, เครื่องใช้ไฟฟ้า, ยานพาหนะ, สิ่งทอ, วัสดุ
            ก่อสร้าง เป็นต้น ดังนั้นสินค้าที่มีตรามอก. คือ สินค้าที่มีคุณภาพได้มาตรฐานตามที่กําหนด มีความปลอดภัยในการอุปโภค บริโภค มีประสิทธิภาพ
            ในการใช้งาน และมีคุณภาพเหมาะสมกับราคา</p>
